package Petle.ForForEach;

public class LiczbyCalkowite {
    public static void main(String[] args) {
        System.out.println("Napisz skrypt, który wyświetli 100 kolejnych liczb całkowitych począwszy od 3");


        for(int i =3; i<=100; i++){
            System.out.println(i);
        }
    }
}
