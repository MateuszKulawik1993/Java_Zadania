package Petle.While;

public class PetlaWhile {
    public static void main(String[] args) {

        int liczenie = 1;
        while (liczenie <= 10) {
            System.out.println("Tekst:" + liczenie);
            liczenie++;
        }

        int i = 1;
        do{
            System.out.println("Tekst z do while:" + i);
            i++;
        }while(i<=10);



    }


}
